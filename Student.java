class Student extends Person {
    private int idNum;
    private double gpa;

    public int getIdNum() {
        return idNum;
    }

    public double getGPA() {
        return gpa;
    }

    public void setIdNum(int idNum) {
        this.idNum = idNum;
    }

    public void setGPA(double gpa) {
        this.gpa = gpa;
    }
}
